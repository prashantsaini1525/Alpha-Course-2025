
public class AirthmeticOperators1 {
    public static void main(String[] args) {
        int A = 40;
        int B = 4;

        System.out.println("Sum of A and B : " + (A + B));
        System.out.println("Difference of A and B : " + (A - B));
        System.out.println("Product of A and B : " + (A * B));
        System.out.println("Division of A by B : " + (A / B));
        System.out.println("Modulus of A by B : " + (A % B));
        System.out.println("Increment of A : " + ++A);
        System.out.println("Decrement of A : " + --A);
        System.out.println("Square of A : " + (A * A));
        System.out.println("Cube of A : " + (A * A * A));
    }
}
