// Description: Assignment operators in Java.


public class assignment {
    public static void main(String[] args) {
        
        int a = 4;
        a += 2; // a = a + 2
        System.out.println(a);

        int b = 4;
        b -= 2; // b = b - 2
        System.out.println(b);

        int c = 4;
        c *= 2; // c = c * 2
        System.out.println(c);

        int d = 4;
        d /= 2; // d = d / 2
        System.out.println(d);

        int e = 4;
        e %= 2; // e = e % 2
        System.out.println(e);
    }
    
}
