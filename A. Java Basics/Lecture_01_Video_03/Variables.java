
public class Variables {

    public static void main(String args[]) {
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);

        /*in below we can see this we can print the string as well*/
        String Name = "Tony Stark"; //string
        System.out.println(Name);

        /*in below you can see this we can change the value of variables */
        a = 50;
        System.out.println(a);

        /*in below we can see this we can assign the value of b to a */
        a = b;
        System.out.println(a);
    }

}
