public class SumofAandB {
    public static void main(String args[]){
        int a = 10;
        int b = 5;

        int sum = a + b;
        System.out.println(sum);

        int sub = a - b;
        System.out.println(sub);

        int mul = a * b;
        System.out.println(mul);

        int div = a / b;
        System.out.println(div);

        int mod = a % b;
        System.out.println(mod);
    }
}
